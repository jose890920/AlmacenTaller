    public void mapearTotales(){
                            
                    
                    listadoTotalesPago = pagoEmpleadoDAO.consultarTotalesPago
                    (numeroDocumentoEmpleadoPagoTxt.getText(), fechaInicioDate.getDate(), fechaFinDate.getDate());
                    
                    for (int i = 0; i < listadoTotalesPago.size(); i++) {
                        if (i == 0) {
                            descuentosNominaLbl.setText(listadoTotalesPago.get(i).toString());
                        }else if(i == 1){
                        subTotalPagoLbl.setText(listadoTotalesPago.get(i).toString());
                        
                        }
                        
                }
                    descuentoTallerLbl.setText((Double.parseDouble(subTotalPagoLbl.getText())*0.25)+"");
                    totalPagoLbl.setText(Double.parseDouble(subTotalPagoLbl.getText())-
                    (Double.parseDouble(descuentosNominaLbl.getText())+Double.parseDouble(descuentoTallerLbl.getText()))+"");
                    pagarNominaBtn.setVisible(true);
    }

