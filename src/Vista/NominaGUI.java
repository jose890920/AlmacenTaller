    private void consultarServiciosPagarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarServiciosPagarBtnActionPerformed
       
       
                   persona = new Persona();
            
            empleado = new Empleado();
            if (!validaciones.validarCampoVacio(numeroDocumentoEmpleadoPagoTxt) &&
                 fechaInicioDate.getDate() != null && fechaFinDate.getDate() != null &&
                 fechaInicioDate.getDate().compareTo(fechaFinDate.getDate()) <= 0 ) {
             persona.setNumeroDocumento(numeroDocumentoEmpleadoPagoTxt.getText());
             
                
             empleado.setPersona(persona);
            
            try{
            if (facadeEmpleado.consultarEmpleado(empleado) != null) {
                    empleado = facadeEmpleado.consultarEmpleado(empleado);
                    nombreEmpleadoLbl.setText(empleado.getPersona().getNombres().trim()+
                        " "+empleado.getPersona().getApellidos().trim());
                    listaParametrosPagoEmpleado = pagoEmpleadoDAO.consultarServiciosPagoEmpleadonumeroDocumento
                    (numeroDocumentoEmpleadoPagoTxt.getText(),fechaInicioDate.getDate(),fechaFinDate.getDate());
                    mapearServiciosRealizados();
                    mapearTotales();

                }
            } catch (SQLException ex) {
                Logger.getLogger(NominaGUI.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ParseException ex) {
                Logger.getLogger(NominaGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
             
        }
       
        
    }//GEN-LAST:event_consultarServiciosPagarBtnActionPerformed

