     public PagoEmpleado mapeoPagoEmpleado(PagoEmpleado pagoEmpleado){
         pagoEmpleado = new PagoEmpleado();
         
         pagoEmpleado.setEmpleado(empleado);
         if (valorDescuentoEmpleadoTxt.getText().trim().equals("")) {
             valorDescuentoEmpleadoTxt.setText("0");
         }
         pagoEmpleado.setDescuento(Double.parseDouble(valorDescuentoEmpleadoTxt.getText()) );
         
         pagoEmpleado.setEstado(constantes.CONSTANTE_ESTADO_POR_DEFECTO);
         
         
         pagoEmpleado.setCodEmpleado(empleado.getCodEmpleado());
         pagoEmpleado.setFechaPago(fechaPagoDate.getDate());
         pagoEmpleado.setValorPago(0.0);
         
         
        return pagoEmpleado;

     }
     
     
