     public void registrarDetallePago(JTable servicios, int codigoPago) throws SQLException, ParseException{
         
         for (int i = 0; i < servicios.getRowCount(); i++) {
             DetallePago detallePago = new DetallePago();
             detallePago.setCodPago(codigoPago);
             detallePago.setTipoServicio(servicios.getValueAt(i, 0).toString());
             detallePago.setValorServicio(Double.parseDouble(servicios.getValueAt(i, 1).toString()));
             detallePago.setDescuentoServicio(Double.parseDouble(servicios.getValueAt(i, 2).toString()));
             detallePago.setDescripcion(servicios.getValueAt(i, 3).toString());
             detallePagoDAO.registrarDetallePago(detallePago);
 
             
             
   
         }
         pagoEmpleado.setCodPago(codigoPago);
         pagoEmpleado.setValorPago(Double.parseDouble(totalParcialLbl.getText()));
         facadePagoEmpleado.modificarPagoEmpleado(pagoEmpleado);
         
     
     }

